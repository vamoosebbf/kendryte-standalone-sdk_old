void i2c_master_init(int num) //1 650, 0 850
{
    if(num)
    {
        fpioa_set_function(40, FUNC_I2C1_SDA); //650
        fpioa_set_function(41, FUNC_I2C1_SDA);
    } else
    {
        fpioa_set_function(40, FUNC_I2C0_SDA); //850
        fpioa_set_function(41, FUNC_I2C0_SCLK);
    }
    i2c_init(num, GC0328_ADDR >> 1, 7, 100000);
}

static void gc0328_wr_reg(uint8_t num, uint8_t reg, uint8_t data)
{
    uint8_t buf[2];

    buf[0] = reg & 0xff;
    buf[1] = data;
    i2c_send_data(num, buf, 2);
}

static uint8_t gc0328_rd_reg(uint8_t num, uint8_t reg)
{
    uint8_t reg_buf[1];
    uint8_t data_buf;

    reg_buf[0] = reg & 0xff;
    i2c_recv_data(num, reg_buf, 1, &data_buf, 1);
    return data_buf;
}

int gc0328_read_id(uint8_t num, uint8_t *id)
{
    *id = gc0328_rd_reg(num, 0xf0);
    return 0;
}

#define sensor_default_regs sensor_test_regs

int gc0328_init(void)
{
    uint8_t data;
    uint32_t i;
    uint8_t num = 0;
    i2c_master_init(num);
    gc0328_read_id(num, &data);
    LOGI(TAG, "ID%d: 0x%x", num, data);

    for(i = 0; sensor_default_regs[i][0]; i++)
    {
        gc0328_wr_reg(num, sensor_default_regs[i][0], sensor_default_regs[i][1]);
        data = gc0328_rd_reg(num, sensor_default_regs[i][0]);
    }
    gc0328_wr_reg(num, 0xFE, 0x00);
    gc0328_wr_reg(num, 0xF1, 0x07);
    gc0328_wr_reg(num, 0xF2, 0x01);
    return 0;
}
